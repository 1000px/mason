class CreateVideoScriptSkill(BaseSkill):
    name = "create_video_script"
    description = "生成视频脚本：让大模型扮演民间传统说书先生，创作民间故事并输出为 video-script-*.json 格式。"

    permissions = {
        "network": False,
        "filesystem": True,
        "max_cpu": 0.1,
        "max_memory": 64,
    }

    def execute(self, **kwargs) -> str:
        os.makedirs(WORKSPACE_DIR, exist_ok=True)

        template = random.choice(STORY_TEMPLATES)
        title = template["title"]

        roles = ["旁白 default"]
        for r in template["roles"]:
            roles.append(f"{r['name']} {r['gender']}")

        script = []
        for scene in template["scenes"]:
            script.append({
                "text": scene["text"],
                "image_prompt": scene["image_prompt"],
                "captions": scene["captions"],
            })

        project_id = uuid.uuid4().hex

        data = {
            "meta": {
                "title": title,
                "project_id": project_id,
                "roles": roles,
            },
            "script": script,
        }

        timestamp = int(time.time())
        file_path = os.path.join(WORKSPACE_DIR, f"video-script-{timestamp}.json")

        with open(file_path, "w", encoding="utf-8") as f:
            json.dump(data, f, ensure_ascii=False, indent=2)

        scene_count = len(script)
        caption_count = sum(len(s.get("captions", [])) for s in script)
        role_names = [r["name"] for r in template["roles"]]

        return (
            f"📜 民间故事脚本已生成！\n\n"
            f"📁 文件: {file_path}\n"
            f"📖 故事标题: 《{title}》\n"
            f"📖 故事主题: {template['theme']}\n"
            f"👥 角色: 旁白, {', '.join(role_names)}\n"
            f"📊 段落数: {scene_count} 段\n"
            f"💬 字幕单元: {caption_count} 条\n\n"
            f"💡 接下来可以：\n"
            f'  - "批量生成语音" — 为所有角色配音\n'
            f'  - "语音转字幕" — 生成精确时间轴字幕\n'
            f'  - "生成剪映草稿" — 一键生成剪映专业版草稿'
        )


def create_skill():
    return CreateVideoScriptSkill()